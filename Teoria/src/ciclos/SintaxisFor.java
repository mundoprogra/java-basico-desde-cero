package ciclos;

public class SintaxisFor {
    /**
     * 
     * for(iniVariable; condicion(es); camVariable)
     * {
     *      instrucciones;
     * }
     * 
     */
    
    public static void main(String[] args)
    {
        for(int c = 1; c <= 10; c++)
        {
            System.out.println("Hola Mundo " + c);
        }
    }
}
